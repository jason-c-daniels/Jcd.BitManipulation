namespace Jcd.BitManipulation;

/// <summary>
/// A type indicating which endian is in use.
/// </summary>
public enum Endian
{
   /// <summary>
   /// Indicates that Little Endian is in use.
   /// </summary>
   Little = 0

  ,

   /// <summary>
   /// Indicates that Big Endian is in use.
   /// </summary>
   Big = 1
}